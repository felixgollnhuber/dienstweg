import { test, after } from "node:test";
import assert from "node:assert/strict";
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { join } from "node:path";
import { defaultConfig } from "../src/config.mjs";
import {
  generatedFiles,
  renderAgentsBlock,
  renderGeneratedFile,
  mergeSettings,
  mergeCodexHooks,
  hookWiredIn,
  codexHookWiredIn,
  wireHooks,
  orphanedHarnessArtifacts,
} from "../src/generate.mjs";
import { tmpRepo, cleanupAll, ROOT } from "./helpers.mjs";

after(cleanupAll);

const cfg = (over = {}) =>
  defaultConfig({
    project: "demo",
    language: "en",
    harnesses: ["claude", "codex"],
    linearTeam: "Demo",
    issuePrefix: "DEM",
    defaultProject: "",
    baseBranch: "main",
    buildGates: "npm test",
    autoMerge: true,
    highRisk: [],
    singleWriter: [],
    ...over,
  });

const targets = (harnesses) => generatedFiles(harnesses).map((s) => s.target);

test("generatedFiles filters by active harness", () => {
  assert.deepEqual(targets(["claude"]), [
    ".claude/commands/create-issue.md",
    ".claude/commands/start-task.md",
    ".claude/hooks/branch-guard.mjs",
  ]);
  assert.deepEqual(targets(["codex"]), [
    ".agents/skills/create-issue/SKILL.md",
    ".agents/skills/start-task/SKILL.md",
    ".codex/hooks/branch-guard.mjs",
  ]);
  assert.equal(targets(["claude", "codex"]).length, 6);
  assert.deepEqual(targets([]), []);
  assert.deepEqual(targets(undefined), []);
});

test("both harnesses share the same branch-guard template", () => {
  const specs = generatedFiles(["claude", "codex"]);
  const guards = specs.filter((s) => s.kind === "mjs");
  assert.equal(guards.length, 2);
  assert.equal(new Set(guards.map((s) => s.template)).size, 1, "one shared template");
});

test("renderAgentsBlock resolves all tokens and labels harnesses", () => {
  const out = renderAgentsBlock(cfg());
  assert.match(out, /Harnesses:\*\* Claude Code \+ Codex/);
  assert.doesNotMatch(out, /\{\{[a-zA-Z]+\}\}/, "no unresolved tokens");
  // Review paragraph is harness-neutral (no subagent_type leak).
  assert.doesNotMatch(out, /subagent_type=/);
  assert.match(out, /codex exec/);
});

test("renderAgentsBlock renders reviewer stances in the review paragraph", () => {
  const out = renderAgentsBlock(cfg());
  assert.match(out, /distinct stance/);
  assert.match(out, /adversarial, spec-conformance, maintainer/);
  assert.match(out, /round-robin/);
});

test("renderAgentsBlock reflects custom stances from config", () => {
  const c = cfg();
  c.review.stances = ["security", "performance"];
  assert.match(renderAgentsBlock(c), /security, performance/);
});

test("committed start-task templates instruct per-reviewer stances", () => {
  for (const rel of [
    "templates/claude/commands/start-task.md",
    "templates/codex/skills/start-task/SKILL.md",
  ]) {
    const body = readFileSync(join(ROOT, rel), "utf8");
    assert.match(body, /stance/i, `${rel} should reference reviewer stances`);
    assert.match(body, /config\.review\.stances/, `${rel} should read config.review.stances`);
    assert.match(body, /round-robin/, `${rel} should describe round-robin assignment`);
    assert.match(body, /spec-conformance/, `${rel} should name the spec-conformance stance`);
  }
});

